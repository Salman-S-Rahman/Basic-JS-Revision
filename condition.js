var number = 50;

if (number >= 90) {
    console.log("You Got A+");
} else if (number >= 80 && number < 90) {
    console.log("You Got A");
} else if (number >= 70 && number < 80) {
    console.log("You Got A-");
} else if (number >= 60 && number < 70) {
    console.log("You Got B+");
} else if (number >= 50 && number < 60) {
    console.log("You Got B");
} else {
    console.log("You Have Failed");
}