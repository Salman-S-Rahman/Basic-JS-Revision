var factorial = 1;
for (var i = 1; i <= 5; i++) {
  var factorial = factorial * i;
}
console.log(factorial);

// ===== factorial in iterative way by loop =========

var factorial = 1;
var i = 1;
while (i <= 10) {
  factorial = factorial * i;
  i++;
}
console.log(factorial);

